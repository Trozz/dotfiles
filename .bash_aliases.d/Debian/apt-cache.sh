alias aptadd='sudo apt-cache add'
alias aptdepends='apt-cache depends' # shows package dependancy
alias aptrdepends='apt-cache rdepends' # shows dependancy in reverse order
alias aptdotty='sudo apt-cache dotty' # creates graph data for Dotty (GraphViz)
alias aptdumpavail='sudo apt-cache dumpavail' # prints out an available list to stdout.
alias aptdump='apt-cache dump' # short listing of all packages
alias aptgencaches='sudo apt-cache gencaches' # creates APT's package cache
alias aptpkgnames='apt-cache pkgnames' # prints the name of each package APT knows
alias aptpolicy='apt-cache policy' # policy is meant to help debug issues relating to the preferences file
alias aptsearch='apt-cache search' # full text search on all available package lists
alias aptshowpkg='apt-cache showpkg'
alias aptshowsrc='apt-cache showsrc'
alias aptshow='apt-cache show' # show performs a function similar to dpkg --print-avail
alias aptstats='apt-cache stats' # stats displays some statistics about the cache
alias aptunmet='apt-cache unmet' # unmet displays a summary of all unmet dependencies in the package cache


